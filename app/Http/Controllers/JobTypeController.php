     */
    public function store(Request $request)
    {
        $job_type = new JobType;
        $job_type->title = $request->title;
        $job_type->save();
        if($request->ajax()){
            return response()->json(array('status'=>'success','job_type'=>$job_type));
        }
        return redirect(action('JobController@index'));
    }

